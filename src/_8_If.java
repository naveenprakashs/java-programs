
public class _8_If
{

	public static void main(String[] args)
	{
		int a=10;
		
		// if condition must be boolean expression
		
		if(a==10)
		{
			System.out.println("a is equal to 10");
		}
		
		if(a>100); // it executes because of semicolon even though the condition is not true
		{
			System.out.println("a is bigger than 100");
		}
		
		
		if(a==10)
		System.out.println("a is equal to 10"); 
		
	/*	if() condition is essential for if control statement
		{
			System.out.println(a);
		}
	*/	
	}

}
